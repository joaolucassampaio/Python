# O operador aritmético "+" pode ser usado como concantenação que, na programação,
# significa a junção de strings. Exemplo:
concatenacao = 'Joao' + ' ' + 'Lucas' 
print(concatenacao)

# O operador aritmético "*" pode ser usado como repetição. Exemplo:
a_dez_vezes = 'A' * 10
print(a_dez_vezes)