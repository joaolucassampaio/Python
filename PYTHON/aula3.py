'''
Phyton = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''

# Aspas Simples
print('Joao Lucas')

# Aspas Duplas
print("Joao Lucas")

# Escape
print("Joao \"Lucas\"") # Diz para o interpretador que o dígito não precisa ser interpretado.

# r
print(r"Joao \"Lucas\"") # Caso queira mostrar o \

# Forma mais limpa
print('Joao "Lucas"') # O interpretador aceita aspas duplas dentro de simples e vice-versa

